<h1>Bones MVC</h1>

<p>This is the default page for the Bones MVC. Here are a list of features for developers.</p>

<ul>
    <li>Seperates data from presentation</li>
    <li>Message flashing system built in</li>
    <li>Can deny access to certain users on different views</li>
    <li>Can log important information into daily text files</li>
    <li>Base model allows for functionality such a creating a new database record</li>
</ul>