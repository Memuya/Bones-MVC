<h1>Bones MVC</h1>

<p>This is the default page for the Bones MVC framework. Here are a list of features for developers.</p>

<ul>
    <li>Seperates data from presentation</li>
    <li>Dynamic routing system built in</li>
    <li>Message flashing system built in</li>
    <li>Simple to use validation system</li>
    <li>Middleware on routes</li>
    <li>Can log important information into daily text files</li>
    <li>Simple redirect system</li>
    <li>Base model allows for basic functionality such a creating a new database records</li>
</ul>
